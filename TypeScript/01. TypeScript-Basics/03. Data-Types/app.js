// ---------- boolean: ----------
var isEnabled = true;
var isAlive = false;
console.log(isEnabled);
console.log(isAlive);
// ---------- number: ----------
var age = 35;
var height = 1.754;
console.log(age);
console.log(height);
// decimal:
var decimal = 6;
// hex:
var hex = 0xf00d; // 61453 in decimal
// binary:
var binary = 10; // 10 in decimal
// octal:
var octal = 484; // 484 in decimal
console.log(decimal);
console.log(hex);
console.log(binary);
console.log(octal);
// ---------- string: ----------
var firstName = 'Tom';
var lastName = 'Johns';
var fullName = firstName + " " + lastName;
console.log(fullName);
// ---------- any: ----------
var someVar = 'hello';
console.log(someVar);
console.log(typeof (someVar)); // string
someVar = 25;
console.log(someVar);
console.log(typeof (someVar)); // number
